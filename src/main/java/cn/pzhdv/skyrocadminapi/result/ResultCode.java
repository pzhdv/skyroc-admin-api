package cn.pzhdv.skyrocadminapi.result;

import lombok.Getter;

/**
 * 接口响应状态码枚举类
 * <p>
 * 核心设计规范：
 * 1. 遵循HTTP状态码语义，扩展业务子码，格式为「主码+3位子码」（如401001=未登录）；
 * 2. 分类管理状态码，便于维护和前端对接：
 *    - 2xx：成功状态（基础成功200）；
 *    - 4xx：客户端错误（参数、权限、资源不存在等）；
 *    - 5xx：服务端错误（系统异常、第三方服务调用失败等）；
 * 3. 支持动态占位符替换（【{}】），适配个性化错误提示（如「用户【admin】不存在」）；
 * 4. 错误信息语义化，前端可直接展示，无需二次解析。
 * <p>
 * 使用规范：
 * - 成功响应：仅使用SUCCESS(200)，不扩展成功子码；
 * - 客户端错误：优先使用细分码（如PARAM_REQUIRED替代BAD_REQUEST），提升错误定位精度；
 * - 服务端错误：避免暴露敏感信息（如SQL错误仅返回「服务器内部错误」，详细日志记录到服务端）；
 * - 动态提示：通过message(Object... params)方法填充占位符，如USER_NOT_EXIST.message("1001") → "用户【1001】不存在"。
 *
 * @author PanZonghui
 * @version 1.0
 * @since 2025-12-31
 */
@Getter
public enum ResultCode {
    // ========================= 基础成功状态 =========================
    /** 通用成功状态码 - 所有接口处理成功的默认返回码 */
    SUCCESS(200, "请求成功"),

    // ========================= 客户端错误 (4xx) =========================
    /* 400：请求参数错误（子码：001-099）- 客户端提交的参数格式/内容不符合要求 */
    /** 通用请求参数错误 - 未明确具体错误类型时使用 */
    BAD_REQUEST(400, "请求参数错误"),
    /** 参数格式/逻辑异常（如手机号格式错误、数值超出范围） */
    PARAM_INVALID(400001, "参数异常"),
    /** JSON解析失败（请求体格式非合法JSON） */
    JSON_PARSE_ERROR(400002, "JSON解析异常"),
    /** 必传参数缺失 - 占位符填充具体字段名（如「【手机号】不能为空」） */
    PARAM_REQUIRED(400003, "【{}】不能为空"),
    /** 文件上传请求异常（如请求体无文件、请求格式错误） */
    FILE_UPLOAD_FAIL(400004, "文件上传请求异常"),

    /* 401：未授权访问（子码：001-099）- 身份认证失败/未登录 */
    /** 通用未授权错误 - 未明确具体认证失败原因时使用 */
    UNAUTHORIZED(401, "未经授权的访问"),
    /** 用户未登录 - 访问需要认证的接口时未携带令牌 */
    NOT_LOGIN(401001, "用户未登录，请先登录"),
    /** 登录失败 - 用户名/密码错误 */
    LOGIN_FAILED(401002, "用户名或密码错误"),
    /** 令牌过期 - AccessToken失效，需刷新或重新登录 */
    TOKEN_EXPIRED(401003, "登录凭证已过期"),
    /** 令牌无效 - 令牌格式错误/签名验证失败/篡改/解析异常 */
    TOKEN_INVALID(401004, "无效的身份令牌"),
    /** 刷新令牌过期 - RefreshToken失效，需重新登录 */
    REFRESH_TOKEN_EXPIRED(401005, "登录超时，请重新登录"),
    /** 账号锁定 - 因多次错误操作导致账号临时锁定 */
    ACCOUNT_LOCKED(401006, "账号已被锁定"),

    /* 403：禁止访问（子码：001-099）- 已认证但无权限执行操作 */
    /** 通用禁止访问 - 未明确具体权限缺失原因时使用 */
    FORBIDDEN(403, "禁止访问"),
    /** 操作权限不足 - 如普通用户尝试执行管理员操作 */
    OPERATION_REJECTED(403001, "无权限执行此操作"),
    /** 资源占用冲突 - 如修改已被他人占用的资源 */
    RESOURCE_OCCUPIED(403002, "资源已被占用"),

    /* 404：资源不存在（子码：001-099）- 请求的资源未找到 */
    /** 通用资源不存在 - 未明确具体资源类型时使用 */
    NOT_FOUND(404, "资源不存在"),
    /** 资源已删除 - 资源曾存在但已被删除 */
    RESOURCE_DELETED(404001, "资源已被删除"),
    /** 用户不存在 - 占位符填充用户名/用户ID（如「用户【1001】不存在」） */
    USER_NOT_EXIST(404002, "用户【{}】不存在"),
    /** 文件不存在 - 占位符填充文件名/文件路径（如「文件【test.jpg】不存在」） */
    FILE_NOT_FOUND(404003, "文件【{}】不存在"),

    /* 405：不支持的请求方法 - 如GET请求调用仅支持POST的接口 */
    METHOD_NOT_ALLOWED(405, "不支持的请求方法"),

    /* 413：请求实体过大（文件大小相关，子码：001-099） */
    /** 通用请求实体过大 - 未明确具体超限类型时使用 */
    PAYLOAD_TOO_LARGE(413, "请求实体过大"),
    /** 单文件大小超限 - 占位符填充配置的最大大小（如「单文件大小超过限制（最大10MB）」） */
    FILE_TOO_LARGE(413001, "单文件大小超过限制（最大{}）"),
    /** 总文件大小超限 - 占位符填充配置的最大大小（如「总文件大小超过限制（最大100MB）」） */
    FILE_TOTAL_MAX_ERROR(413002, "总文件大小超过限制（最大{}）"),

    /* 415：不支持的媒体类型（文件类型相关） */
    /** 通用不支持的媒体类型 - 未明确具体类型时使用 */
    UNSUPPORTED_MEDIA_TYPE(415, "不支持的媒体类型"),
    /** 不支持的文件类型 - 占位符填充具体文件类型（如「不支持的文件类型【.exe】」） */
    UNSUPPORTED_FILE_TYPE(415001, "不支持的文件类型【{}】"),

    /* 422：请求参数验证失败（子码：001-099）- 参数格式合法但业务验证失败 */
    /** 通用参数验证失败 - 未明确具体失败原因时使用 */
    UNPROCESSABLE_ENTITY(422, "请求参数验证失败"),
    /** 数据重复（唯一约束）- 占位符填充重复数据内容（如「数据【admin】已存在」） */
    DUPLICATE_DATA(422001, "数据【{}】已存在"),
    /** 数据完整性冲突 - 如外键关联数据不存在 */
    DATA_INTEGRITY_ERROR(422002, "数据完整性冲突"),
    /** 数据不存在 - 业务操作依赖的数据未找到 */
    DATA_NOT_FOUND(422003, "数据不存在"),
    /** 数据重复（唯一约束）- 占位符填充数量信息（如「数据重复，不符合唯一约束（当前2条，允许1条）」） */
    DATA_DUPLICATE(422004, "数据重复，不符合唯一约束（当前{}条，允许1条）"),

    /* 429：请求过于频繁 - 触发接口限流/防刷机制 */
    TOO_MANY_REQUESTS(429, "请求过于频繁，请稍后再试"),

    // ========================= 服务端错误 (5xx) =========================
    /* 500：服务器内部错误（子码：001-099）- 服务端处理逻辑异常 */
    /** 通用服务器内部错误 - 未明确具体异常类型时使用 */
    SERVER_ERROR(500, "服务器内部错误"),
    /** SQL语法错误 - 仅日志记录，前端展示「服务器内部错误」 */
    SQL_SYNTAX_ERROR(500001, "SQL语法错误"),
    /** 数据添加失败 - 如插入数据库时异常 */
    ADD_FAIL(500002, "数据添加失败"),
    /** 数据修改失败 - 如更新数据库时异常 */
    UPDATE_FAIL(500003, "数据修改失败"),
    /** 数据删除失败 - 如删除数据库记录时异常 */
    DELETE_FAIL(500004, "数据删除失败"),
    /** 数据查询失败 - 如查询数据库时异常 */
    QUERY_FAIL(500005, "数据查询失败"),
    /** 用户注册失败 - 注册流程中出现异常 */
    REGISTER_FAIL(500006, "用户注册失败"),
    /** 文件删除失败 - 如删除服务器/COS文件时异常 */
    FILE_DELETE_ERROR(500007, "文件删除失败"),
    /** 保存草稿失败 - 如文章/表单草稿保存异常 */
    DRAFT_FAIL(500008, "保存草稿失败"),

    /* 502：网关错误（第三方服务相关，子码：001-099）- 调用外部服务失败 */
    /** 通用第三方服务调用失败 - 未明确具体服务时使用 */
    BAD_GATEWAY(502, "第三方服务调用失败"),
    /** AI服务调用失败 - 如调用大模型/图片生成接口失败 */
    AI_SERVICE_ERROR(502001, "AI服务调用失败"),
    /** 短信服务异常 - 如发送验证码短信失败 */
    SMS_SERVICE_ERROR(502002, "短信服务异常"),
    /** 邮件发送失败 - 如发送验证邮件/通知邮件失败 */
    EMAIL_SERVICE_ERROR(502003, "邮件发送失败"),

    /* 503：服务不可用 - 如服务熔断/降级、维护中 */
    SERVICE_UNAVAILABLE(503, "服务不可用，请稍后再试"),

    // ========================= 验证码相关 (归422，子码：101-199) =========================
    /** 邮箱格式错误 - 如缺少@、域名不合法 */
    EMAIL_FORMAT_ERROR(422101, "邮箱格式错误"),
    /** 邮箱验证码错误 - 用户输入的验证码与系统生成的不一致 */
    EMAIL_CODE_ERROR(422102, "邮箱验证码有误"),
    /** 邮箱验证码过期 - 验证码超出有效期 */
    EMAIL_CODE_INVALID(422103, "邮箱验证码已过期"),
    /** 邮箱验证码发送失败 - 如邮件服务器异常、收件人地址错误 */
    EMAIL_SEND_ERROR(422104, "邮箱验证码发送失败"),
    /** 图片验证码错误 - 用户输入的验证码与图片展示的不一致 */
    IMAGE_CODE_ERROR(422105, "图片验证码有误"),
    /** 图片验证码过期 - 验证码超出有效期 */
    IMAGE_CODE_INVALID(422106, "图片验证码已过期"),
    /** 图片验证码生成失败 - 如生成验证码图片时异常 */
    IMAGE_CODE_GENERATE_ERROR(422107, "图片验证码生成失败"),

    // ========================= 文件上传辅助校验 (归422，子码：201-299) =========================
    /** 上传文件为空 - 用户未选择任何文件提交上传请求 */
    FILE_NULL_ERROR(422201, "上传失败，请选择至少一个文件");

    /** 状态码（主码+子码，如401001）
     * -- GETTER --
     *  获取状态码
     *
     */
    private final int code;
    /** 错误提示信息（支持【{}】占位符）
     * -- GETTER --
     *  获取原始错误信息（含占位符）
     *
     */
    private final String message;

    /**
     * 枚举构造方法
     * @param code 状态码
     * @param message 错误提示信息
     */
    ResultCode(int code, String message) {
        this.code = code;
        this.message = message;
    }

    /**
     * 动态填充错误信息中的占位符（【{}】）
     * <p>
     * 支持多占位符顺序填充，示例：
     * - USER_NOT_EXIST.message("1001") → "用户【1001】不存在"
     * - 若占位符数量多于参数，未填充的占位符保留为【{}】；
     * - 若参数数量多于占位符，多余参数忽略。
     *
     * @param params 占位符参数（顺序与占位符一一对应）
     * @return 填充后的完整错误信息
     */
    public String message(Object... params) {
        if (params == null || params.length == 0) {
            return this.message;
        }
        String filledMsg = this.message;
        // 循环替换所有占位符（支持多个占位符场景）
        for (Object param : params) {
            filledMsg = filledMsg.replaceFirst("\\{}", String.valueOf(param));
        }
        return filledMsg;
    }
}